a = 57
b = 17

# Bitwise OR (|)

print("\n=============OR============")

c = a|b

print("nilai :",a,", binary :",format(a,"08b"))

print("nilai :",b,", binary :",format(b,"08b"))

print("______________________________ ● (|)")

print("nilai :",c,", binary :",format(c,"08b"))



# Bitwise AND (&)

print("\n\n=============AND============")

d = a&b

print("nilai :",a,", binary :",format(a,"08b"))

print("nilai :",b,", binary :",format(b,"08b"))

print("______________________________ ● (&)")

print("nilai :",d,", binary :",format(d,"08b"))



# Bitwise XOR (^)

print("\n\n=============XOR============")

e = a^b

print("nilai :",a,", binary :",format(a,"08b"))

print("nilai :",b,", binary :",format(b,"08b"))

print("______________________________ ● (^)")

print("nilai :",e,", binary :",format(e,"08b"))


# CONTOH
print("\n=============CONTOH XOR==============")

x = 0b00111111
y = 0b10110011

print("nilai :",x^y,", binary :",format(x^y,"08b"))



# Bitwise NOT (~)

print("\n\n=============NOT============")

f = ~a

print("nilai :",a,", binary :",format(a,"08b"))

print("______________________________ ● (~)")

print("nilai :",f,", binary :",format(f,"08b"))



# bitwise Shift Right (>>)

print("\n\n=============(>>)============")

g = a >> 2

print("nilai :",a,", binary :",format(a,"08b"))

print("______________________________ ● (>>)")

print("nilai :",g,", binary :",format(g,"08b"))



# bitwise Shift left (<<)

print("\n\n=============(<<)============")

h = a << 2

print("nilai :",a,", binary :",format(a,"08b"))

print("______________________________ ● (<<)")

print("nilai :",h,", binary :",format(h,"08b"))












































