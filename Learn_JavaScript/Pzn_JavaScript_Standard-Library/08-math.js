// Math isn't Constructor
console.log(Math);

// Macam-macam fungsi pada Math
console.log(Math.max(1,2,3,4)); // 4 => Mencari nilai maksimum
console.log(Math.min(1,2,3,4)); // 1 => Mencari nilai minimum
console.log(Math.floor(1.2)); 	// 1 => Pembulatan kebawah
console.log(Math.ceil(1.2));		// 2 => Pembulatan keatas
console.log(Math.round(1.4)); 	// 1 => if x <= 4 = kebawah (Pembulatan normal) 
console.log(Math.round(1.5)); 	// 2 => if x >= 5 = keatas (Pembulatan normal)







